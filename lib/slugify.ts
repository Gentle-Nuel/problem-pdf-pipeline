// Simple, dependency-free slugify — good enough for short English problem
// titles; not meant to handle every unicode edge case.
export function slugify(text: string): string {
  return text
    .toLowerCase()
    .trim()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 80);
}
